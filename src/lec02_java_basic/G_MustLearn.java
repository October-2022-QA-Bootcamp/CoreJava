package lec02_java_basic;

// Memorize it please
public class G_MustLearn { // starting of the class body
	
	// This is a class body
	// First: Class body contain variables
	// Those variables can be either declared (no value assigned) or initialized (value assigned) 	
	public String name; // variable declared
	public String myName = "Tofael"; // variable initialized, variable type String, this represent any noun/description
	public int myAge = 99; // variable type int, this represent number
	public char mySex = 'M'; // variable type char, this represent character
	public boolean usCitizen = true; // variable type boolean, this represent true or false
	
	// Second: Class body also contains Constructor
	// Those Constructor can be either declared or initialized
	
	// Third: Class body also contains methods
	// Those methods can be either implemented or initialized. (very important info)
	// Inside the class, method can't be declared  (very important info)
	
	
} // Ending of the class body
