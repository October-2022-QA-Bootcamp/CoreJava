package j02_java_basic;

//use of " " ---> \"  \"
//use of ' ' ---> \'  \'
//use of \ \ ---> \\   \\

public class F_use_of_single_or_double_quotation_inside_String {
	public static void main(String[] args) {
		System.out.println("Kamala Harris, American lawyer, politician and 49th Vice President of the United States (2021-) who was the 1st African-American, Asian-American attorney general of California, born in Oakland, California.");
		System.out.println("\n---------------------------------------------------------------------------------------------------------------------------------------\n");
		System.out.println("\"Kamala Harris\", American lawyer, politician and 49th Vice President of the United States (2021-) who was the \'1st African-American\', \'Asian-American attorney general\' of California, born in \\Oakland\\, California.");
	}

}
